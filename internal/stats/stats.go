package stats

import "time"

func MinResponseTime(durations []time.Duration) time.Duration {
	if len(durations) == 0 {
		return 0
	}

	min := durations[0]
	for _, d := range durations {
		if d < min {
			min = d
		}
	}

	return min
}


func MaxResponseTime(durations []time.Duration) time.Duration {
	if len(durations) == 0 {
		return 0
	}

	max := durations[0]
	for _, d := range durations {
		if d > max {
			max = d
		}
	}

	return max
}

func AverageResponseTime(durations []time.Duration) time.Duration {
	if len(durations) == 0 {
		return 0
	}

	var total time.Duration
	for _, d := range durations {
		total += d
	}

	return total / time.Duration(len(durations))
}
